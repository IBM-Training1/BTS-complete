package com.ibm;

import javax.validation.constraints.NotNull;

public class Project {
	@NotNull
	private String id;
	@NotNull
	private String name;
	@NotNull
	private String type;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
